texto_usuario = input("Ingrese un texto: ")
texto_mayuscula = texto_usuario.upper()
texto_minuscula = texto_usuario.lower()
print("El texto en mayuscula: ", texto_mayuscula)
print("El texto en minuscula: ", texto_minuscula)