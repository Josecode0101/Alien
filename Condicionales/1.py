# Titulo: Positivo, negativo o cero
# Descripcion: Escribe un programa que solicite 
# al usuario ingresar un número y determine si el 
# número es positivo, negativo o cero.
# Fecha de creacion: Martes, 01 de octubre
# Autor: Jose Benito Ondo Ndong

numero = int(input("Ingrese un numero: "))
if numero >0:
    print("El numero es positivo")
elif numero <0:
    print("El numero es negativo")
else:
    print("El numero es cero")
